package hu.me;

public class Szamologep{

    public Szamologep() {}

    public int osszeadas(int a, int b) {
        return a+b;
    }

    public int kivonas(int a, int b) {
        return a-b;
    }

    public int szorzas(int a, int b) {
        return a*b;
    }

    public int osztas(int a, int b) {
        return a/b;
    }

}
